#include "3-calc.h"

/**
 * op_add - adds two integers
 * @a: the first int
 * @b: the second int
 *
 * Return: the sum
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two integers
 * @a: the first int
 * @b: the second int
 *
 * Return: the difference
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul -  multiple two integes
 * @a: the fisrt int
 * @b: the secount int
 *
 * Return: the multibly
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides two integers
 * @a: the first int
 * @b: the secound int
 *
 * Return: the quotient
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo two integers
 * @a: he first int
 * @b: the second int
 *
 * Return: the remainer
*/
int op_mod(int a, int b)
{
	return (a % b);
}

